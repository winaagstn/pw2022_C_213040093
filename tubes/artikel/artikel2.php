        <figcaption class="figure-caption">Gigi Susu Mulai Goyang, Saatnya ke Dokter Gigi</figcaption>
        <div class="card-body text-start">
          <p>
          Gigi susu merupakan gigi pertama anak sebelum tumbuhnya gigi permanen. Pertumbuhannya sendiri memiliki urutan tertentu. Meski nantinya akan tanggal dan digantikan dengan gigi permanen, gigi susu perlu dirawat dengan baik karena akan memengaruhi pertumbuhan gigi permanen kelak. Gigi susu memiliki banyak fungsi, di antaranya:
          </p>
          <ul>
            <li>Sebagai penjaga ruang gigi permanen.</li>
            <li>Sebagai pembentuk wajah</li>
            <li>Sebagai pembantu anak berbicara dengan jelas, serta mengunyah makanan.</li>
          </ul>
          <p>
          Gigi susu umumnya tumbuh ketika anak berusia antara 6-24 bulan. Meski umum tumbuh pada usia anak 6 bulan, pada sebagian anak, gigi susu bisa tumbuh saat usianya kurang dari 6 bulan. Sedangkan, gigi permanen biasanya akan tumbuh secara bertahap saat anak berusia 6-7 tahun.
          </p>
          <p class="display-5">
            Saat Gigi Susu Mulai Goyang
          </p>
          <p>
          Gigi susu yang mulai goyang adalah pertanda bahwa harus segera dicabut. Sebenarnya, penanganan di rumah dapat dilakukan, yaitu dengan menggoyangkan gigi tersebut setiap hari. Jika kondisi gigi dan mulut aman, ibu dapat mencabutnya dengan menggunakan kapas dingin atau benang yang diikat di bagian gigi yang goyang, kemudian cabut secara cepat. Jika tidak ingin mengambil risiko, biarkan gigi susu copot dengan sendirinya. Ibu juga bisa menemui dokter gigi di rumah sakit terdekat untuk mencabut gigi susu. Serahkan pada ahlinya, agar tidak menimbulkan masalah kesehatan lain, atau trauma pada anak.
          </p>
          <p class="display-5">
            Tidak Ada Ruang untuk Tumbuhnya Gigi Permanen
          </p>
          <p>
          Ukuran rahang yang kecil umumnya disertai dengan ukuran gigi susu yang juga kecil. Padahal, ukuran gigi permanen yang nantinya akan tumbuh bisa saja memiliki ukuran yang jauh lebih besar daripada gigi susu sebelumnya. Persediaan ruang yang kurang memadai akan membuat gigi permanen dan gigi susu saling menumpuk dan terlihat tidak rapi. Bukan itu saja, gigi permanen juga bisa saja sulit keluar karena tidak memiliki cukup ruang, karena terhalang oleh gigi susu yang lain. Satu-satunya pilihan untuk memperbaiki struktur gigi adalah dengan pemasangan kawat gigi. Kawat gigi bukan hanya berfungsi untuk meratakan gigi saja, tapi juga untuk memperbesar ukuran rahang yang kecil.
          </p>
          <p class="display-5">
          Saat Gigi Susu Mengalami Karies
          </p>
          <p>
          Saat gigi susu anak mengalami pengikisan atau berlubang karena terlalu sering mengonsumsi makanan manis, mereka bisa saja mengalami gigi karies. Karies pada gigi akan menyebabkan rasa sakit yang dapat berujung pada anak yang terus menangis dan rewel. Jika sudah begitu, sebaiknya ibu temui dokter untuk melakukan pencabutan gigi. 
          </p>
          <p class="display-5">
          Saat Gigi Susu Mengalami Infeksi
          </p>
          <p>
          Saat gigi susu mengalami kerusakan parah akibat infeksi, kerusakan biasanya akan meluas sampai ke bagian pulpa, yaitu lapisan paling dalam pada gigi setelah enamel dan dentin yang tersusun dari pembuluh darah, saraf, serta jaringan lunak lainnya. Saat kondisi tersebut terjadi, bakteri akan lebih mudah masuk dan tinggal di dalam pulpa. Jika efek rasa sakit akibat bakteri tersebut tidak dapat mereka dengan pemberian antibiotik, maka mencabut gigi susu menjadi pilihan terbaik.
          </p>
