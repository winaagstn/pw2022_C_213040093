          <p>
            Ibarat anak, yang lahir paling terakhir di suatu keluarga, biasanya disebut sebagai bungsu. Gigi bungsu pun demikian, karena tumbuh paling terakhir, yaitu sekitar usia 17-21 tahun. Bila gigi bungsu tumbuh miring, hal ini bisa jadi menimbulkan masalah. 
            Gigi bungsu yang tumbuh miring atau ke arah yang salah, bisa melukai jaringan di sekitarnya, seperti gusi hingga pipi bagian dalam. Kondisi ini memicu rasa nyeri dan pembengkakan. Lantas, haruskah gigi bungsu yang tumbuh miring dioperasi?
          </p>
            <p class="display-5">Mengatasi Gigi Bungsu Tumbuh Miring</p>
          <p>
          Karena tumbuh di usia dewasa, biasanya sudah tidak ada lagi ruang tersisa di rahang untuk ditempati gigi bungsu. Akibatnya, gigi bungsu yang berusaha keluar akhirnya tumbuh miring menempati sela-sela kosong yang tersedia. Namun, gigi bungsu tidak selalu miring. Jika ukuran rahang masih cukup untuk menampung, gigi bungsu bisa tumbuh lurus dan tidak menimbulkan masalah. Pada kondisi ini, pencabutan atau operasi tidak diperlukan. Di dalam istilah medis, gigi bungsu tumbuh miring disebut juga impaksi gigi bungsu. Satu-satunya cara untuk mengatasinya melalui prosedur operasi. Meski terdengar menakutkan, operasi gigi bungsu termasuk dalam prosedur yang ringan. Prosedur ini memakan waktu yang singkat dan tidak diperlukan rawat inap setelahnya. Jadi, operasi gigi bungsu sebenarnya hampir sama dengan pencabutan gigi. Hanya saja, dokter gigi biasanya melakukan sedikit pengurangan jaringan di sekitar gigi bungsu. Bila gigi bungsu masih dalam kondisi terinfeksi, prosedur operasi biasanya tidak bisa langsung dilakukan. Dokter biasanya akan melakukan penanganan terlebih dahulu, dengan memberi obat-obatan, berupa antibiotik dan pereda nyeri. Setelah infeksi sembuh, barulah operasi bisa dilakukan. 
          </p>
          <p>
          Operasi gigi bungsu juga bisa dilakukan sebelum adanya masalah yang muncul. Inilah pentingnya rutin memeriksakan gigi, setidaknya satu kali dalam setahun. Bila dokter menjumpai ada potensi gigi bungsu untuk tumbuh miring, tindakan operasi bisa dilakukan untuk pencegahan infeksi. Jadi, jangan lupa untuk rutin melakukan pemeriksaan ke dokter gigi
          </p>
