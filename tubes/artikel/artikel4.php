        <div class="card-body text-start">
          <p>
          Gigi berlubang bukan hanya masalah orang dewasa saja, tetapi juga bisa terjadi pada anak-anak. Gejala gigi berlubang pada anak-anak umumnya sama dengan yang terjadi pada orang dewasa, tetapi anak dengan gigi berlubang mungkin juga akan bersikap rewel dan mengalami demam yang mengindikasikan adanya infeksi. Yuk, simak penjelasannya lebih lanjut di bawah ini. Seperti yang sudah kita semua ketahui, kesehatan gigi yang terjaga dengan baik akan menghasilkan gigi dan gusi yang sehat serta senyum dan napas yang segar. Sebaliknya, kebersihan gigi yang buruk dapat membuat kamu berisiko mengalami gigi rusak, seperti gigi berlubang atau busuk. Kerusakan gigi biasanya disebabkan oleh plak yang merupakan lapisan lengket dan tidak berwarna yang terbentuk pada enamel gigi. Enamel adalah permukaan luar gigi yang keras. Nah, ketika plak yang mengandung bakteri ini bercampur dengan gula yang ada dalam makanan, ia akan menciptakan asam yang menggerogoti gigi. Kondisi inilah yang dapat mengakibatkan gigi berlubang atau karies.
          </p>
          <p class="display-5">Gejala-Gejala Gigi Berlubang pada Anak</p>
          <ul>
            <li>Bintik-bintik putih mulai muncul pada gigi di daerah yang terkena. Bintik-bintik ini menandakan bahwa enamel gigi sudah mulai rusak. </li>
            <li>Rongga awal mulai terbentuk pada gigi. Rongga ini biasanya berwarna cokelat muda.</li>
            <li>Bila tidak segera diobati, rongga bisa berkembang menjadi lebih dalam dan warnanya berubah menjadi hitam.</li>
          </ul>
          <p>Gigi berlubang bisa saja tidak menimbulkan gejala apapun. Kadang-kadang, anak-anak baru mengetahui bahwa gigi mereka berlubang saat memeriksakan kondisi giginya ke dokter. Namun, gigi berlubang juga dapat menyebabkan anak mengalami gejala berikut ini:</p>
          <ul>
            <li>Rasa sakit di daerah gigi yang berlubang</li>
            <li>Sensitivitas terhadap makanan tertentu, seperti permen dan makanan panas atau dingin.</li>
            <li>Anak menjadi lebih rewel, lekas marah, dan demam yang menandakan adanya infeksi.</li>
