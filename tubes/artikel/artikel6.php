        <figcaption class="figure-caption">Gigi Berlubang</figcaption>
        <div class="card-body text-start">
          <p>Gigi berlubang adalah kondisi gigi yang rusak akibat terkikisnya lapisan terluar gigi (enamel). Kondisi ini disebabkan oleh penumpukan bakteri di mulut akibat sering mengonsumsi makanan manis dan tidak menjaga kebersihan mulut. Gigi berlubang merupakan keluhan pada gigi yang umum terjadi, baik pada orang dewasa maupun anak-anak. Kondisi ini sulit terdeteksi karena umumnya tidak menimbulkan nyeri di awal. Oleh sebab itu, pemeriksaan gigi perlu dilakukan secara berkala. Gigi berlubang yang tidak segera diatasi dapat berkembang menjadi semakin besar. Hal ini dapat meningkatkan risiko terjadinya masalah gigi yang lain, seperti infeksi dan gigi tanggal.</p>
          <p class="display-5">Penyebab dan Faktor Resiko Gigi Berlubang</p>
          <p>Gigi berlubang berawal dari plak yang menempel di gigi. Plak gigi berasal dari sisa makanan yang mengandung gula dan pati. Jika tidak dibersihkan, plak ini akan diubah menjadi asam oleh bakteri alami di dalam mulut. Asam yang dihasilkan dari plak kemudian secara perlahan mengikis lapisan terluar gigi. Lama-kelamaan, lubang pun terbentuk pada gigi. Jika masih dibiarkan, bakteri dan asam akan masuk lebih dalam sampai ke pulpa gigi, yaitu bagian gigi yang terdiri dari saraf dan pembuluh darah.</p>
          <p>Gigi berlubang dapat terjadi pada siapa saja, tetapi ada beberapa faktor yang membuat seseorang lebih berisiko mengalaminya, yaitu:</p>
          <ul>
            <li>Jarang menyikat atau membersihkan gigi, terutama setelah makan</li>
            <li>Tidak menggunakan pasta gigi atau obat kumur yang mengandung fluoride untuk membersihkan gigi</li>
            <li>Terlalu banyak mengonsumsi makanan dan minuman manis (seperti kue, biskuit, permen, dan es krim) atau asam (seperti minuman bersoda)</li>
            <li>Menderita gangguan makan, seperti anoreksia dan bulimia</li>
            <li>Menderita penyakit refluks asam lambung (GERD) dan mulut kering</li>
            <li>Berusia lanjut sehingga enamel mulai menipis dengan sendirinya dan produksi air liur berkurang</li>
            <li>Rutin mengonsumsi obat-obatan, suplemen, vitamin, atau produk herba yang mengandung gula</li>
          </ul>
          <p class="display-5">Gejala Gigi Berlubang</p>
          <ul>
            <li>Gigi menjadi sensitif</li>
            <li>Sakit gigi ketika menggigit</li>
            <li>Nyeri di gigi yang terjadi secara spontan tanpa sebab yang jelas</li>
            <li>Ngilu atau nyeri saat mengonsumsi makanan atau minuman yang manis, dingin, atau panas</li>
            <li>Terdapat lubang yang terlihat jelas di gigi</li>
            <li>Noda putih, cokelat, atau hitam pada permukaan gigi</li>
          </ul>
