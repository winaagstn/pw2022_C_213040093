          <p class="display-5">Cara Mudah menjaga kesehatan gigi dan mulut</p>
          <ul>
            <li><strong> menggosok gigi terlalu keras</strong></li>
            <p>
              Salah satu tujuan sikat gigi adalah menghilangkan plak gigi. Namun, jika Anda menyikat gigi terlalu keras, gesekannya dapat merobek gusi dan mengikis enamel gigi yang relatif tipis. Akibatnya, gigi Anda jadi lebih sensitif. Selain itu, cara sikat gigi yang tidak benar dapat menyebabkan plak gigi malah menumpuk dan mengeras yang dapat berakibat pada gingivitis (peradangan gusi). Menyikat gigi haruslah dilakukan secara lembut dengan gerakan memutar dan memijat gigi. Biasanya, lama durasi yang efektif untuk sikat gigi adalah sekitar dua menit.</p>
            <li> <strong>Sikat gigi sebelum tidur</strong> </li>
            <p>Sikat gigi sebelum tidur ternyata dapat menghilangan kuman dan plak pada gigi Anda yang menumpuk lama sepanjang hari. Selain menyikat gigi, Anda juga dianjurkan untuk menyikat lidah demi menghilangkan kuman atau plak yang menempel pada lidah.</p>
            <li> <strong>Gunakan pasta gigi berfluorida</strong> </li>
            <p>Fluorida adalah unsur alami yang dapat ditemukan di banyak hal, seperti air minum dan makanan yang Anda konsumsi. Fluorida diserap tubuh untuk digunakan oleh sel-sel yang membangun gigi Anda untuk menguatkan enamel gigi. Fluorida juga merupakan pertahanan utama terhadap kerusakan gigi yang bekerja dengan memerangi kuman yang dapat menyebabkan kerusakan, serta menyediakan perlindungan alami untuk gigi Anda. Oleh karena itu, gunakanlah pasta gigi yang mengandung fluorida.</p>
            <li><strong>Jangan Merokok</strong> </li>
            <p>Tembakau dapat menyebabkan gigi menguning dan bibir menghitam. Merokok juga melipatgandakan risiko Anda terhadap penyakit gusi dan kanker mulut. Oleh karena itu, berhenti merokok sekarang juga.</p>
            <li><strong>Minum Air yang Banyak</strong> </li>
            <p>Air merupakan minuman terbaik untuk kesehatan Anda secara keseluruhan, termasuk bagi kesehatan mulut Anda karena aktivitas minum dapat membantu membersihkan beberapa efek negatif dari makanan dan minuman yang menempel pada gigi Anda.</p>
            <li> <strong>Batasi konsumsi makanan manis dan asam</strong> </li>
            <p>Makanan manis dan asam akan diubah menjadi asam oleh bakteri di mulut yang kemudian dapat menggerogoti enamel gigi Anda. Asam inilah yang menyebabkan gigi Anda cepat berlubang.</p>
            <li> <strong>Makan makanan yang bergizi</strong> </li>
            <p>Makan makanan yang bergizi juga baik untuk kesehatan gigi dan mulut Anda. Makan makanan yang bergizi — termasuk biji-bijian, kacang-kacangan, buah-buahan dan sayuran, dan produk susu — dapat memberikan semua nutrisi yang Anda butuhkan. Bahkan, sebuah studi menemukan bahwa omega-3 lemak — jenis lemak sehat dalam makanan laut— dapat dapat mengurangi risiko peradangan, sehingga dapat menurunkan risiko penyakit gusi.</p>
