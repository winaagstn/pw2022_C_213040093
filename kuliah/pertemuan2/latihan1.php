<?php
// VARIABEL
// tempat penyimpanan nilai
// Nilai; angka (integer), tulisan (string), boolean (true/false)

// Nama bebas, tidak boleh ada spasi
// boleh ada angka, tapi tidak diawal
$nama = "WinaAgustina";
echo $nama;
echo "<hr>";



// OPERATOR
// Aritmatika
// +, -, *, / (kalibataku), % (modulo,modulus,sisa bagi)
echo 10 * 10 + 2 - 1;
echo "<br>";
echo 2 % 2;
echo "<hr>"

// Perbandingan
// <, >, <=, >=, ==, !=
echo 1 < 2; //true / 1
echo "<br>"
echo 10 < 5; //false / null
echo "<br>"
echo 10 == 10;
echo "<hr>"

// Identitas
// ===, !==
// mengecek nilai dan tipe data
echo 10 === "10";
echo "<hr>"

// Increment / Decrement
// Penambahan / Pengurangan
// ++, --
$x = 10;

echo $x++;
echo --$x;
echo "<br>";
echo ++$x;

// Concat, Penggabung String
// .
$nama_depan = "Wina";
$nama_belakang ="Agustina"
echo $nama_depan . $nama_belakang
?>

